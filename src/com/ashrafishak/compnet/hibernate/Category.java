package com.ashrafishak.compnet.hibernate;

public class Category {

	private int id;
	private String category;
	
	public Category() {}

	public void setId(int id) {
		this.id = id;
	}

	public int getId() {
		return id;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getCategory() {
		return category;
	}
	
	
}
