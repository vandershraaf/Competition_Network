package com.ashrafishak.compnet.hibernate;

public class Tag {

	private int id;
	private String name;
	
	public Tag() {}

	public void setId(int id) {
		this.id = id;
	}

	public int getId() {
		return id;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}
	
	
}
